class Instrumento:
    def __init__(self, nombre, marca, modelo, imagen, precio, costoEnvio, cantidadVendida, descripcion):
        self.nombre = nombre
        self.marca = marca
        self.modelo = modelo
        self.imagen = imagen
        self.precio = precio
        self.costoEnvio = costoEnvio
        self.cantidadVendida = cantidadVendida
        self.descripcion = descripcion
